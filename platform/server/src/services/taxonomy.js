/** 某类目下允许的工种列表（未登记的类目返回空数组） */
export function tradesForCategory(category) {
  return CATEGORY_TRADES[category] || []
}

/** 扁平化的工种全集（去重）——供 C 端任务大厅工种筛选下拉使用，保证 B/C 两端工种词表一致 */
